def pt1(input: str):
    """Part 1"""
    brackets = []
    bracket_map = {
        ")": "(",
        "]": "[",
        "}": "{",
        ">": "<",
    }
    scores = {
        ")": 3,
        "]": 57,
        "}": 1197,
        ">": 25137,
    }

    score = 0
    lines = input.splitlines()

    for line in lines:
        for char in line:
            if char in bracket_map.values():
                brackets.append(char)
            else:
                if brackets.pop() != bracket_map[char]:
                    score += scores[char]
                    break

    return score


def pt2(input: str):
    """Part 2"""
    brackets = []
    bracket_map = {
        ")": "(",
        "]": "[",
        "}": "{",
        ">": "<",
    }
    r = {
        "(": ")",
        "[": "]",
        "{": "}",
        "<": ">",
    }
    char_scores = {
        ")": 1,
        "]": 2,
        "}": 3,
        ">": 4,
    }

    scores = []
    lines = input.splitlines()

    for line in lines:
        brackets = []
        for char in line:
            if char in bracket_map.values():
                brackets.append(char)
            else:
                if brackets.pop() != bracket_map[char]:
                    break
        else:
            score = 0
            while brackets:
                score *= 5
                score += char_scores[r[brackets.pop()]]
            scores.append(score)

    scores.sort()
    return median(scores)

